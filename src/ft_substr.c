#include <stddef.h>
#include <stdlib.h>

size_t	ft_strlen(const char *str);

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*r;
	size_t	i;

	if (!s)
		return (0);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(&s[start]))
		len = ft_strlen(&s[start]);
	r = malloc(len + 1);
	if (!r)
		return (0);
	i = -1;
	while (++i < len)
		r[i] = s[start + i];
	r[i] = 0;
	return (r);
}
