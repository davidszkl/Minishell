#include "../inc/minishell.h"

char	*ft_tolower(char *str)
{
	int	n;

	n = 0;
	while (str[n])
	{
		if (str[n] >= 'A' && str[n] <= 'Z')
			str[n] += 32;
		n++;
	}
	return (str);
}
